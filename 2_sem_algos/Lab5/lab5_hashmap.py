class HashMapTimer:

    timings = {
        'setitem': [],
        'getitem': [],
        'delitem': []
    }


    @staticmethod
    def time_counter(method_name):
        def decorator(func):
            @wraps(func)
            def wrapper(self, *args, **kwargs):

                current_ff = self._check_filling_factor()

                start = time.perf_counter()
                result = func(self, *args, **kwargs)
                duration = time.perf_counter() - start

                HashMapTimer.timings[method_name].append((current_ff, duration))

                return result
            return wrapper
        return decorator



