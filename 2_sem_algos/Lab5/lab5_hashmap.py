import time
import matplotlib.pyplot as plt
import numpy as np
from typing import Any
from functools import wraps



class Node:

    __slots__ = ('key', 'data', 'next')

    def __init__(self, key, data = None):
        self.key = key
        self.data = data
        self.next = None



class LinkedList:

    def __init__(self):
        self.head = None


    def add_to_start(self, key, data):

        cur = self.head
        while cur is not None:
            if cur.key == key:
                cur.data = data
                return True
            cur = cur.next

        new = Node(key, data)
        if self.head is None:
            self.head = new
            return False
        new.next = self.head
        self.head = new

        return False


    def rm_node_etot(self, key):

        if self.head is None:
            return False
        if self.head.key == key:
            self.head = self.head.next
            return True
        cur = self.head
        while cur.next is not None:
            if cur.next.key == key:
                cur.next = cur.next.next
                return True
            cur = cur.next

        return False



