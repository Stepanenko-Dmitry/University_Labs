def timer_decorator(func_name: str = None):

    def decorator(func: Callable) -> Callable:

        @wraps(func)
        def wrapper(*args, **kwargs):

            start = time.perf_counter()
            result = func(*args, **kwargs)
            end = time.perf_counter()
            elapsed_ms = (end - start) * 1000
            name = func_name if func_name else func.__name__

            return result, elapsed_ms, name

        return wrapper

    return decorator



def merge(left: List[int], right: List[int]) -> List[int]:

    result = []
    i = j = 0

    while i < len(left) and j < len(right):
        if left[i] <= right[j]:
            result.append(left[i])
            i += 1
        else:
            result.append(right[j])
            j += 1

    result.extend(left[i:])
    result.extend(right[j:])

    return result


@timer_decorator("Слияние (ст.)")
def merge_sort_standard(arr: List[int]) -> List[int]:

    if len(arr) <= 1:
        return arr
    mid = len(arr) // 2
    left = merge_sort_standard(arr[:mid])[0]
    right = merge_sort_standard(arr[mid:])[0]

    return merge(left, right)


@timer_decorator("Быстрая (ст.)")
def quick_sort_standard(arr: List[int]) -> List[int]:

    if len(arr) <= 1:
        return arr
    pivot = arr[len(arr) // 2]
    left = [x for x in arr if x < pivot]
    middle = [x for x in arr if x == pivot]
    right = [x for x in arr if x > pivot]

    return quick_sort_standard(left)[0] + middle + quick_sort_standard(right)[0]


@timer_decorator("Python sorted()")
def builtin_sort(arr: List[int]) -> List[int]:
    return sorted(arr)



def merge_optimized(arr: List[int], temp: List[int], left: int, mid: int, right: int) -> None:

    i, j, k = left, mid + 1, left
    while i <= mid and j <= right:
        if arr[i] <= arr[j]:
            temp[k] = arr[i]
            i += 1
        else:
            temp[k] = arr[j]
            j += 1
        k += 1
    while i <= mid:
        temp[k] = arr[i]
        i += 1
        k += 1
    while j <= right:
        temp[k] = arr[j]
        j += 1
        k += 1
    for i in range(left, right + 1):
        arr[i] = temp[i]


def merge_sort_optimized_internal(arr: List[int], temp: List[int], left: int, right: int) -> None:

    if left >= right:
        return
    mid = (left + right) // 2
    merge_sort_optimized_internal(arr, temp, left, mid)
    merge_sort_optimized_internal(arr, temp, mid + 1, right)
    merge_optimized(arr, temp, left, mid, right)


@timer_decorator("Слияние (опт.)")
def merge_sort_optimized(arr: List[int]) -> List[int]:

    arr_copy = arr.copy()
    temp = [0] * len(arr_copy)
    merge_sort_optimized_internal(arr_copy, temp, 0, len(arr_copy) - 1)
    return arr_copy


def quick_sort_optimized_internal(arr: List[int], low: int, high: int) -> None:

    if low >= high:
        return

    if high - low < 20:
        insertion_sort(arr, low, high)
        return

    mid = (low + high) // 2
    if arr[low] > arr[mid]:
        arr[low], arr[mid] = arr[mid], arr[low]
    if arr[low] > arr[high]:
        arr[low], arr[high] = arr[high], arr[low]
    if arr[mid] > arr[high]:
        arr[mid], arr[high] = arr[high], arr[mid]

    pivot = arr[mid]
    arr[mid], arr[high] = arr[high], arr[mid]

    i = low - 1
    for j in range(low, high):
        if arr[j] <= pivot:
            i += 1
            arr[i], arr[j] = arr[j], arr[i]

    arr[i + 1], arr[high] = arr[high], arr[i + 1]
    pivot_index = i + 1

    quick_sort_optimized_internal(arr, low, pivot_index - 1)
    quick_sort_optimized_internal(arr, pivot_index + 1, high)


def insertion_sort(arr: List[int], low: int, high: int) -> None:

    for i in range(low + 1, high + 1):
        key = arr[i]
        j = i - 1
        while j >= low and arr[j] > key:
            arr[j + 1] = arr[j]
            j -= 1
        arr[j + 1] = key


@timer_decorator("Быстрая (опт.)")
def quick_sort_optimized(arr: List[int]) -> List[int]:

    arr_copy = arr.copy()
    quick_sort_optimized_internal(arr_copy, 0, len(arr_copy) - 1)

    return arr_copy


@timer_decorator("Кучей")
def heap_sort(arr):
    heapq.heapify(arr)
    result = []
    while arr:
        result.append(heapq.heappop(arr))
    return result



