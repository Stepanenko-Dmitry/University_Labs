from typing import Optional, Any



class Node:
    def __init__( self, value ):
        self.value = value
        self.left = None
        self.right = None


def depth( root: Optional[Node], value: Any, current_depth = 0 ) -> int:
    if root is None:
        return -1

    if root.value == value:
        return current_depth

    left_depth = depth( root.left, value, current_depth + 1 )
    if left_depth != -1:
        return left_depth

    return depth( root.right, value, current_depth + 1 )


def lca( root: Optional[Node], a: Any, b: Any ) -> Optional[Any]:
    if root is None:
        return None

    if root.value == a or root.value == b:
        return root.value

    left = lca( root.left, a, b )
    right = lca( root.right, a, b )

    if left and right:
        return root.value

    return left or right


def mirror( root: Optional[Node] ) -> None:
    if root is None:
        return

    mirror( root.left )
    mirror( root.right )

    root.left, root.right = root.right, root.left


def dist( root: Node, a: Any, b: Any) -> int:

    LCA = lca( root, a, b )

    return depth(root, a) + depth(root, b) - 2*depth(root, LCA)


def print_tree(node, prefix="", is_left=True):

    if node is not None:
        print_tree(node.right, prefix + ("│   " if is_left else "    "), False)
        print(prefix + ("└── " if is_left else "┌── ") + str(node.value))
        print_tree(node.left, prefix + ("    " if is_left else "│   "), True)



