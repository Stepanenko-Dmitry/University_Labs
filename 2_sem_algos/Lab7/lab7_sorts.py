def linked_merge_sort( lst: LinkedList ) -> LinkedList:

    if len(lst) <= 1:
        return lst

    mid = len(lst) // 2
    left = linked_merge_sort(lst[:mid])
    right = linked_merge_sort(lst[mid:])

    return merge(left, right)


def merge(left: LinkedList, right: LinkedList) -> LinkedList:

    result = LinkedList()
    i = j = 0

    while i < len(left) and j < len(right):
        if left[i] < right[j]:
            result.add_to_end(left[i])
            i += 1
        else:
            result.add_to_end(right[j])
            j += 1

    while i < len(left):
        result.add_to_end(left[i])
        i += 1

    while j < len(right):
        result.add_to_end(right[j])
        j += 1

    return result



