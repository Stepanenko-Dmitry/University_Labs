from typing import Any



class Node:
    def __init__( self, data = None ):
        self.data = data
        self.next = None



class LinkedList:

    def __init__( self ):
        self.head = None
        self.__length = 0


    def __len__( self ) -> int:

        return self.__length


    def __getitem__( self, key: int ) :

        if isinstance( key, slice ):
            if key.step is not None and key.step != 1:
                raise ValueError( "Slice step must be 1" )

            start, stop = key.start, key.stop

            length = len( self )

            if start is None:
                start = 0
            elif start < 0:
                start = length + start
                if start < 0:
                    start = 0

            if stop is None:
                stop = length
            elif stop < 0:
                stop = length + stop
                if stop < 0:
                    stop = 0

            if start >= length or stop <= start:
                return LinkedList()

            if stop > length:
                stop = length

            result = LinkedList()

            current = self.head
            for _ in range( start ):
                if current is None:
                    return result
                current = current.next

            for _ in range( start, stop ):
                if current is None:
                    break
                result.add_to_end( current.data )
                current = current.next

            return result

        elif isinstance( key, int ):
            if key < 0:
                key = len( self ) + key
            if key < 0 or key >= len( self ):
                raise IndexError( "Index out of range" )

            current = self.head
            for _ in range( key ):
                current = current.next
            return current.data


    def contain( self, data: Any ) -> None:

        LastNode = self.head

        while LastNode:
            if LastNode.data == data:
                return True
            else:
                LastNode = LastNode.next

        return False


    def add_to_start( self, data: Any ) -> None:

        new = Node( data )

        if self.head is None:
            self.head = new
            self.__length += 1
            return

        new.next = self.head
        self.head = new
        self.__length += 1

        return


    def add_to_end (self, data: Any ) -> None:

        new = Node( data )
        if self.head is None:
            self.head = new
            self.__length += 1
            return

        cur = self.head

        while cur.next:
            cur = cur.next

        cur.next = new
        self.__length += 1

        return


    def rm_from_start( self ) -> None:

        if self.head is None:
            print( 'Список пуст' )
            return

        start_node = self.head
        self.head = start_node.next
        start_node.next = None
        self.__length -= 1

        return


    def rm_from_end( self ) -> None:

        if self.head is None:
            print( 'Список пуст' )
            return

        if self.head.next is None:
            self.head = None
            self.__length -= 1
            return

        prelast = self.head

        while prelast.next.next:
            prelast = prelast.next

        prelast.next = None
        self.__length -= 1

        return


    def rm_node_etot( self, node: Node ) -> None:

        if self.head is None:
            print( 'Список пуст' )
            return

        cur = self.head

        while cur.next.data != node.data:
            cur = cur.next

        cur.next = node.next
        node.next = None
        self.__length -= 1

        return


    def rm_node( self, idx: int ) -> None:

        if self.head is None:
            print( 'Список пуст' )
            return

        cur_idx = 0
        cur = self.head

        while cur_idx + 1 != idx:
            if cur.next is None:
                print( f'Индекса {idx} нет' )
                return
            cur = cur.next
            cur_idx += 1

        ncur = cur.next
        cur.next = cur.next.next
        ncur.next = None
        self.__length -= 1

        return


    def add_node( self, idx, node ) -> None:

        if self.head is None:
            self.head = node
            return

        cur_idx = 0
        cur = self.head

        while cur_idx + 1 != idx:
            if cur.next is None:
                cur.next = node
                node.next = None
                return
            cur = cur.next
            cur_idx += 1

        ncur = cur.next
        cur.next = node
        node.next = ncur
        self.__length += 1

        return


    def out( self ) -> None:
        cur = self.head
        while cur != None:
            print( cur.data, end = ' --> ' )
            cur = cur.next
        print( 'None' )
        return



