class ComparisonCounter:

    def __init__(self):
        self.count = 0

    def reset(self):
        self.count = 0

    def compare(self, a, b):
        self.count += 1
        return a < b

def natural_merge_sort(arr, counter=None):

    if counter is None:
        counter = ComparisonCounter()

    n = len(arr)
    if n <= 1:
        return arr.copy(), counter.count

    work_arr = arr.copy()
    temp_arr = arr.copy()

    while True:
        runs = []
        i = 0
        while i < n:
            start = i
            i += 1
            while i < n and not counter.compare(work_arr[i], work_arr[i-1]):
                i += 1
            runs.append((start, i))

        if len(runs) <= 1:
            break

        new_runs = []
        for j in range(0, len(runs), 2):
            if j + 1 < len(runs):
                merged_start, merged_end = merge_runs(
                    work_arr, temp_arr,
                    runs[j][0], runs[j][1],
                    runs[j+1][0], runs[j+1][1],
                    counter
                )
                new_runs.append((merged_start, merged_end))
            else:
                new_runs.append(runs[j])

        work_arr, temp_arr = temp_arr, work_arr
        runs = new_runs

        if len(runs) <= 1:
            break

    return work_arr, counter.count

def merge_runs(arr, temp, left1, right1, left2, right2, counter):
    i, j, k = left1, left2, left1

    while i < right1 and j < right2:
        if counter.compare(arr[i], arr[j]):
            temp[k] = arr[i]
            i += 1
        else:
            temp[k] = arr[j]
            j += 1
        k += 1

    while i < right1:
        temp[k] = arr[i]
        i += 1
        k += 1

    while j < right2:
        temp[k] = arr[j]
        j += 1
        k += 1

    return left1, k

def classic_merge_sort(arr, counter=None):
    if counter is None:
        counter = ComparisonCounter()

    if len(arr) <= 1:
        return arr.copy(), counter.count

    mid = len(arr) // 2
    left, _ = classic_merge_sort(arr[:mid], counter)
    right, _ = classic_merge_sort(arr[mid:], counter)

    result = []
    i = j = 0
    while i < len(left) and j < len(right):
        if counter.compare(left[i], right[j]):
            result.append(left[i])
            i += 1
        else:
            result.append(right[j])
            j += 1

    result.extend(left[i:])
    result.extend(right[j:])

    return result, counter.count

def quick_sort(arr, counter=None):
    if counter is None:
        counter = ComparisonCounter()

    if len(arr) <= 1:
        return arr.copy(), counter.count

    first = arr[0]
    middle = arr[len(arr) // 2]
    last = arr[-1]

    if counter.compare(first, middle):
        if counter.compare(middle, last):
            pivot = middle
        else:
            pivot = last if counter.compare(first, last) else first
    else:
        if counter.compare(first, last):
            pivot = first
        else:
            pivot = last if counter.compare(middle, last) else middle

    left = [x for x in arr if counter.compare(x, pivot)]
    middle = [x for x in arr if x == pivot]
    right = [x for x in arr if counter.compare(pivot, x)]

    left_sorted, _ = quick_sort(left, counter)
    right_sorted, _ = quick_sort(right, counter)

    return left_sorted + middle + right_sorted, counter.count

def generate_arrays(size):

    random_arr = [random.randint(0, size) for _ in range(size)]
    sorted_arr = list(range(size))
    reverse_arr = list(range(size, 0, -1))
    return random_arr, sorted_arr, reverse_arr

def test_sorting_algorithms(sizes):
    results = {
        'size': [],
        'type': [],
        'natural_merge': [],
        'classic_merge': [],
        'quick_sort': []
    }

    for size in sizes:
        print(f"\nРазмер массива: {size}")
        random_arr, sorted_arr, reverse_arr = generate_arrays(size)

        for arr_type, arr in [('Случайный', random_arr),
                               ('Отсортированный', sorted_arr),
                               ('Обратный', reverse_arr)]:

            counter = ComparisonCounter()
            _, natural_comp = natural_merge_sort(arr.copy(), counter)

            counter = ComparisonCounter()
            _, classic_comp = classic_merge_sort(arr.copy(), counter)

            counter = ComparisonCounter()
            _, quick_comp = quick_sort(arr.copy(), counter)

            results['size'].append(size)
            results['type'].append(arr_type)
            results['natural_merge'].append(natural_comp)
            results['classic_merge'].append(classic_comp)
            results['quick_sort'].append(quick_comp)

            print(f"{arr_type:15} | Natural: {natural_comp:8} | Classic: {classic_comp:8} | Quick: {quick_comp:8}")

    return results

