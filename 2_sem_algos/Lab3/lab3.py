class Node:
    def __init__(self, data):
        self.data = data
        self.next = None



class LinkedStack:
    def __init__(self):
        self.top = None

    def push(self, item):
        new_node = Node(item)
        new_node.next = self.top
        self.top = new_node

    def pop(self):
        if self.is_empty():
            raise IndexError("Pop from empty stack")
        item = self.top.data
        self.top = self.top.next
        return item

    def is_empty(self):
        return self.top == None



def CanExit(entering, stack):
    ent = entering
    posled = [i+1 for i in range(len(ent))]
    for car in ent:
        if car in posled:
            for i in range(len(posled)):
                ncar = posled[i]
                stack.push(ncar)
                posled[i] = None
                if ncar == car:
                    break
        p = stack.pop()
        if p != car:
            return False
    return True



