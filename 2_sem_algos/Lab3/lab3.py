class Node:
    def __init__(self, data):
        self.data = data
        self.next = None
        self.prev = None



class LinkedListDeque:
    def __init__(self):
        self.front = None
        self.rear = None
        self.size = 0

    def add_front(self, item):
        new_node = Node(item)
        if self.is_empty():
            self.front = self.rear = new_node
        else:
            new_node.next = self.front
            self.front.prev = new_node
            self.front = new_node
        self.size += 1

    def add_rear(self, item):
            new_node = Node(item)
            if self.is_empty():
                self.front = self.rear = new_node
            else:
                new_node.prev = self.rear
                self.rear.next = new_node
                self.rear = new_node
            self.size += 1


    def remove_front(self):
        if self.is_empty():
            raise IndexError("Deque is empty")
        item = self.front.data
        self.front = self.front.next
        if self.front:
            self.front.prev = None
        else:
            self.rear = None
        self.size -= 1
        return item

    def remove_rear(self):
        if self.is_empty():
            raise IndexError("Deque is empty")
        item = self.rear.data
        self.rear = self.rear.prev
        if self.rear:
            self.rear.next = None
        else:
            self.front = None
        self.size -= 1
        return item

    def is_empty(self):
        return self.size == 0

    def is_polindrom(self):
        if self.is_empty():
            return True
        size = self.size
        step_cnt = size//2
        scur = self.front
        ncur = self.rear
        for _ in range(step_cnt):
            if scur.data != ncur.data:
                return False
            scur = scur.next
            ncur = ncur.prev
        return True

    def out(self):
        cur = self.front
        for i in range(self.size):
            print(cur.data, end = '')
            cur = cur.next
        print()
        return



