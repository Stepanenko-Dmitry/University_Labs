def recursion_bin_search( arr: list, num: int) -> int:

    left_ind, right_ind = 0, len(arr) - 1

    if left_ind <= right_ind:
        mid = ( left_ind + right_ind ) // 2
        if arr[ mid ] < num:
            recursion_bin_search( arr[ mid + 1 : right_ind ], num )
        elif arr[ mid ] > num:
            recursion_bin_search( arr[ left_ind : mid - 1 ], num )
        else:
            return mid

    return -1



