def cicle_bin_search( arr: list, num: int ) -> int:

    left_ind, right_ind = 0, len( arr )

    while left_ind <= right_ind:
        mid = ( left_ind + right_ind ) // 2
        if arr[ mid ] < num:
            left_ind = mid
        elif arr[ mid ] > num:
            right_ind = mid
        else:
            return mid

    return -1



