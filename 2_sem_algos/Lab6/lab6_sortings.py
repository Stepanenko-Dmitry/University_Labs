import copy



def insertion_index_by_bin_search( arr: list, num: int, reverse: bool = False ) -> int:

    left_ind, right_ind = 0, len( arr ) - 1

    while left_ind <= right_ind:
        mid = ( left_ind + right_ind ) // 2

        if reverse:
            if arr[mid] > num:
                left_ind = mid + 1
            else:
                right_ind = mid - 1
        else:
            if arr[ mid ] < num:
                left_ind = mid + 1
            else:
                right_ind = mid - 1

    return left_ind


def insertion_sort( arr: list, reverse: bool = False ) -> list:

    arr = copy.deepcopy( arr )

    for i in range( 1, len( arr ) ):
        insert_index = insertion_index_by_bin_search( arr[:i], arr[ i ], reverse )
        insert_num = arr[ i ]
        for j in range(i, insert_index, -1):
            arr[ j ] = arr[ j - 1]
        arr[ insert_index ] = insert_num

    return arr



