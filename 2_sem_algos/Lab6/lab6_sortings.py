def sort_by_key( arr: list, key_idx: int, reverse: bool = False ) -> list:

    arr = arr[:]
    n = len( arr )

    for i in range( 1, n ):
        cur = arr[ i ]
        j = i - 1

        while j >= 0:
            cur_key = cur[ key_idx ]
            prev_key = arr[ j ][ key_idx ]

            if reverse:
                move = cur_key > prev_key
            else:
                move = cur_key < prev_key

            if move:
                arr[ j + 1 ] = arr[ j ]
                j -= 1
            else:
                break

        arr[ j + 1 ] = cur

    return arr



def multi_key_sort( arr: list, key1, key2, key3, rev1=False, rev2=False, rev3=False ) -> list:

    res = arr[:]

    res = sort_by_key( res, key3, rev3 )
    res = sort_by_key( res, key2, rev2 )
    res = sort_by_key( res, key1, rev1 )

    return res



def search_by_subj_and_grade( arr: list, subject: str, grade: int ) -> list:

    res = []

    for name, subj, grd in arr:
        if subj== subject and grd == grade:
            res.append( (name, subj, grd) )

    return res



def avg_grade_per_subj( arr: list ) -> dict:

    subjects = {}

    for _, subj, grade in arr:
        if not subj in subjects:
            subjects[ subj ] = [ 0, 0 ]
        subjects[ subj ][ 0 ] += grade
        subjects[ subj ][ 1 ] += 1

    avgs = {}

    for subj, ( total, count ) in subjects.items():
        avg = total / count
        avgs[ subj ] = avg

    return avgs



def best_students_per_subject( arr: list):

    student_subject_grades = {}

    for name, subject, grade in arr:
        if name not in student_subject_grades:
            student_subject_grades[ name ] = [ 0, 0 ]
        student_subject_grades[ name ][ 0 ] += grade
        student_subject_grades[ name ][ 1 ] += 1

    best_by_subject = {}

    for name, ( total, count )in student_subject_grades.items():
        avg = total / count
        best_by_subject[ name ] = avg

    return best_by_subject



